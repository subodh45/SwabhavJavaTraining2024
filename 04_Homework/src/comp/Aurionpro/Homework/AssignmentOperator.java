package comp.Aurionpro.Homework;

public class AssignmentOperator {
  public static void main(String[] args) {
	
	  int number1 = 10;
	  
	  System.out.println("number (n) : "+ number1);
	  
	  System.out.println("n += 3 :" + (number1 += 3));
	  
	  System.out.println("n -= 4 :" +(number1 -= 4));
	  
	  System.out.println("n *= 2 :"+ (number1 *=2));
	  
	  System.out.println("n /= 2 :" + (number1/=2));
}
}
